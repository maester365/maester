function walkFiles(dir, predicate = () => true) {
  if (!existsSync(dir)) return [];
  const files = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) files.push(...walkFiles(path, predicate));
    else if (predicate(path)) files.push(path);
  }
  return files.sort();
}

function yamlQuote(value) {
  return JSON.stringify(String(value ?? ""));
}

function escapeTable(value) {
  return String(value ?? "")
    .replaceAll("|", "\\|")
    .replaceAll("<br>", "<br />")
    .replaceAll("\n", "<br />");
}

function normalizeWhitespace(value) {
  return String(value ?? "").replace(/\s+/g, " ").trim();
}

function trimDescription(value, max = 300) {
  const text = normalizeWhitespace(value)
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1")
    .replace(/#{2,6}\s+/g, "")
    .replace(/<br\s*\/?\s*>/gi, " ");
  if (text.length <= max) return text;
  return `${text.slice(0, max - 1).trim()}…`;
}

function unique(values) {
  return [...new Set(values.filter(Boolean).map((value) => String(value).trim()).filter(Boolean))];
}

function parseTags(value = "") {
  const tags = [];
  const tagArgument = value.match(/-Tag\s+(.+?)(?=\s+-\w+|$)/i)?.[1] ?? "";
  const regex = /["']([^"']+)["']/g;
  let match;
  while ((match = regex.exec(tagArgument))) tags.push(match[1]);
  return tags;
}

function normalizeMarkdown(markdown) {
  return markdown
    .replace(/<br>/gi, "<br />")
    .replace(/\[([^\]]+)\]\(#[^)]+\)/g, "$1");
}

function parseCommentHelp(content) {
  const match = content.match(/<#([\s\S]*?)#>/);
  if (!match) return {};
  const sections = {};
  let current = null;
  for (const rawLine of match[1].split(/\r?\n/)) {
    const line = rawLine.replace(/^\s*#?\s?/, "").trimEnd();
    const header = line.match(/^\.(\w+)\b\s*(.*)$/);
    if (header) {
      current = header[1].toLowerCase();
      sections[current] = [];
      if (header[2]) sections[current].push(header[2].trim());
      continue;
    }
    if (current) sections[current].push(line);
  }
  for (const key of Object.keys(sections)) {
    sections[key] = sections[key].join("\n").trim();
  }
  return sections;
}

function stripGeneratedResultPlaceholder(markdown) {
  return markdown
    .replace(/<!--- Results --->[\s\S]*$/i, "")
    .replace(/%TestResult%/g, "")
    .trim();
}

function extractSection(markdown, headingPattern) {
  const lines = markdown.split(/\r?\n/);
  const start = lines.findIndex((line) => headingPattern.test(line.trim()));
  if (start === -1) return "";
  const body = [];
  for (let index = start + 1; index < lines.length; index += 1) {
    if (/^#{2,6}\s+/.test(lines[index])) break;
    body.push(lines[index]);
  }
  return body.join("\n").trim();
}

function loadFunctionDocs() {
  const docs = new Map();
  for (const file of walkFiles(publicRoot, (path) => path.endsWith(".ps1"))) {
    const functionName = file.split(sep).pop().replace(/\.ps1$/i, "");
    const content = readFileSync(file, "utf8");
    const help = parseCommentHelp(content);
    const markdownPath = file.replace(/\.ps1$/i, ".md");
    const markdown = existsSync(markdownPath) ? normalizeMarkdown(stripGeneratedResultPlaceholder(readFileSync(markdownPath, "utf8"))) : "";
    docs.set(functionName.toLowerCase(), {
      functionName,
      functionPath: file,
      markdownPath: existsSync(markdownPath) ? markdownPath : "",
      synopsis: help.synopsis ?? "",
      description: help.description ?? "",
      example: help.example ?? "",
      link: help.link ?? "",
      markdown,
      remediation: extractSection(markdown, /^#{2,6}\s+Remediation action:?$/i),
      relatedLinks: extractSection(markdown, /^#{2,6}\s+Related links?$/i),
    });
  }

  for (const file of walkFiles(join(internalRoot, "eidsca"), (path) => path.endsWith(".md") && !path.endsWith("@template.md"))) {
    const functionName = file.split(sep).pop().replace(/\.md$/i, "");
    const markdown = normalizeMarkdown(stripGeneratedResultPlaceholder(readFileSync(file, "utf8")));
    docs.set(functionName.toLowerCase(), {
      functionName,
      functionPath: existsSync(file.replace(/\.md$/i, ".ps1")) ? file.replace(/\.md$/i, ".ps1") : "",
      markdownPath: file,
      synopsis: "",
      description: "",
      example: "",
      link: "",
      markdown,
      remediation: extractSection(markdown, /^#{2,6}\s+Remediation action:?$/i),
      relatedLinks: extractSection(markdown, /^#{2,6}\s+Related links?$/i),
    });
  }
  return docs;
}

function suiteFrom(testId, tags, filePath) {
  if (testId.startsWith("EIDSCA.")) return "eidsca";
  if (testId.startsWith("CIS.")) return "cis";
  if (testId.startsWith("CISA.") || testId.startsWith("MS.")) return "cisa";
  if (testId.startsWith("ORCA.")) return "orca";
  if (testId.startsWith("MT.")) return "maester";
  const lowerPath = relative(testsRoot, filePath).toLowerCase();
  if (lowerPath.includes("cisa")) return "cisa";
  if (lowerPath.includes("cis")) return "cis";
  if (lowerPath.includes("eidsca")) return "eidsca";
  if (lowerPath.includes("orca")) return "orca";
  if (tags.includes("Maester")) return "maester";
  return "other";
}

function categoryFrom(tags, filePath) {
  const candidates = tags.filter((tag) => !/^(MT\.|CIS\.|CISA\.|MS\.|EIDSCA\.|ORCA\.|L1$|L2$|Maester$|CIS$|CISA$|ORCA$|EIDSCA$)/i.test(tag));
  if (candidates.length > 0) return candidates[0];
  const parts = relative(testsRoot, filePath).split(sep);
  const meaningfulParts = parts.slice(0, -1).filter((part) => !/^(maester|cis|cisa|eidsca|orca)$/i.test(part));
  return meaningfulParts[0] ?? "General";
}

function findFunctionName(block) {
  const matches = [...block.matchAll(/(^|[^\w-])(Test-[A-Za-z0-9_]+)\b/gm)].map((match) => match[2]);
  const ignored = new Set(["Test-Path"]);
  return matches.find((name) => !ignored.has(name)) ?? "";
}

function findTestId(testName, tags) {
  const idPattern = /^(MT\.\d+|CIS\.[A-Za-z0-9.]+|CISA\.[A-Za-z0-9.]+|EIDSCA\.[A-Z0-9]+|ORCA\.\d+(?:\.\d+)?)/i;
  const idFromName = testName.match(new RegExp(`${idPattern.source}:`, "i"))?.[1];
  const idFromTag = tags.find((tag) => idPattern.test(tag));
  return idFromName ?? idFromTag;
}

function parseTests() {
  const tests = [];
  for (const file of walkFiles(testsRoot, (path) => path.endsWith(".Tests.ps1"))) {
    const content = readFileSync(file, "utf8");
    const describes = [];
    for (const match of content.matchAll(/Describe\s+["'][^"']+["']([^\r\n{]*)/gim)) {
      describes.push({ index: match.index ?? 0, tags: parseTags(match[1] ?? "") });
    }

    const itRegex = /It\s+["']([^"']+)["']([^\r\n{]*)\{/gim;
    let match;
    while ((match = itRegex.exec(content))) {
      const testName = match[1].trim();
      const itArguments = match[2] ?? "";
      const blockStart = itRegex.lastIndex;
      const nextIt = content.slice(blockStart).search(/\n\s*It\s+["']/i);
      const block = nextIt === -1 ? content.slice(blockStart) : content.slice(blockStart, blockStart + nextIt);
      const describeTags = describes.findLast((describe) => describe.index < (match.index ?? 0))?.tags ?? [];
      const tags = unique([...describeTags, ...parseTags(itArguments)]);
      const id = findTestId(testName, tags);
      if (!id) continue;
      const rawTitle = testName.replace(new RegExp(`^${id.replaceAll(".", "\\.")}:\\s*`, "i"), "").replace(/\. See https:\/\/maester\.dev\/docs\/tests\/[^\s]+$/i, "").trim();
      tests.push({
        id,
        rawTitle,
        testName,
        tags: unique([id, ...tags]),
        filePath: file,
        functionName: findFunctionName(block),
      });
    }
  }
  return tests;
}

function loadConfig() {
  const config = JSON.parse(readFileSync(configPath, "utf8"));
  const byId = new Map();
  for (const item of config.TestSettings ?? []) {
    byId.set(String(item.Id).toLowerCase(), item);
  }
  return byId;
}

function fallbackFunctionName(test) {
  if (test.functionName) return test.functionName;
  if (test.id.startsWith("EIDSCA.")) return `Test-MtEidsca${test.id.replace("EIDSCA.", "")}`;
  if (test.id.startsWith("ORCA.")) return `Test-ORCA${test.id.replace("ORCA.", "").replaceAll(".", "_")}`;
  return "";
}

function documentationFunctionName(test, functionDocs) {
  if (test.id.startsWith("EIDSCA.")) {
    const functionName = `Test-MtEidsca${test.id.replace("EIDSCA.", "")}`;
    if (functionDocs.has(functionName.toLowerCase())) return functionName;
  }
  return fallbackFunctionName(test);
}

function buildInventory() {
  const configById = loadConfig();
  const functionDocs = loadFunctionDocs();
  const testsById = new Map();
  for (const test of parseTests()) {
    const key = test.id.toLowerCase();
    if (testsById.has(key)) continue;
    const config = configById.get(key) ?? {};
    const functionName = documentationFunctionName(test, functionDocs);
    const doc = functionDocs.get(functionName.toLowerCase()) ?? {};
    const description = doc.markdown
      ? trimDescription(doc.markdown)
      : trimDescription(doc.description || doc.synopsis || config.Title || test.rawTitle);
    const suite = suiteFrom(test.id, test.tags, test.filePath);
    testsById.set(key, {
      id: test.id,
      title: config.Title ?? test.rawTitle,
      rawTitle: test.rawTitle,
      description,
      severity: config.Severity ?? "Unknown",
      suite,
      category: categoryFrom(test.tags, test.filePath),
      tags: test.tags.sort((a, b) => a.localeCompare(b)),
      functionName,
      sourceTestFile: relative(repoRoot, test.filePath),
      sourceFunctionFile: doc.functionPath ? relative(repoRoot, doc.functionPath) : "",
      synopsis: doc.synopsis ?? "",
      helpDescription: doc.description ?? "",
      markdown: doc.markdown ?? "",
      remediation: doc.remediation ?? "",
      relatedLinks: doc.relatedLinks ?? "",
      commandLink: functionName ? `/docs/commands/${functionName}` : "",
    });
  }
  return [...testsById.values()].sort((a, b) => a.id.localeCompare(b.id, undefined, { numeric: true }));
}

function pagePathFor(test) {
  return join(docsTestsRoot, test.suite, `${test.id}.md`);
}

function renderTestPage(test) {
  const keywords = unique(["Maester", "Microsoft 365 security", test.id, test.severity, suiteConfig[test.suite]?.label, test.category, ...test.tags]);
  const body = [];
  body.push(generatedMarker);
  body.push("", `# ${test.id} - ${test.title}`, "");
  body.push("## Overview", "", test.markdown || test.helpDescription || test.description, "");
  body.push("## Test Metadata", "");
  body.push("| Field | Value |", "| --- | --- |");
  body.push(`| Test ID | ${escapeTable(test.id)} |`);
  body.push(`| Severity | ${escapeTable(test.severity)} |`);
  body.push(`| Suite | ${escapeTable(suiteConfig[test.suite]?.label ?? test.suite)} |`);
  body.push(`| Category | ${escapeTable(test.category)} |`);
  if (test.functionName) body.push(`| PowerShell test | [${test.functionName}](${test.commandLink}) |`);
  body.push(`| Tags | ${escapeTable(test.tags.join(", "))} |`, "");
  if (test.remediation && !test.markdown.includes(test.remediation)) {
    body.push("## Remediation", "", test.remediation, "");
  }
  if (test.relatedLinks && !test.markdown.includes(test.relatedLinks)) {
    body.push("## Related Links", "", test.relatedLinks, "");
  }
  body.push("## Source", "");
  body.push(`- Pester test: \`${test.sourceTestFile}\``);
  if (test.sourceFunctionFile) body.push(`- PowerShell source: \`${test.sourceFunctionFile}\``);
  body.push("");

  return `---
title: ${yamlQuote(`${test.id} - ${test.title}`)}
description: ${yamlQuote(test.description)}
slug: /tests/${test.id}
className: generated-test-doc
sidebar_class_name: hidden
hide_table_of_contents: true
keywords:
${keywords.map((keyword) => `  - ${yamlQuote(keyword)}`).join("\n")}
---

${body.join("\n")}`;
}

function renderSuiteIndex(suite, tests) {
  const config = suiteConfig[suite];
  const rows = tests.map((test) => `| [${test.id}](../${test.id}) | ${escapeTable(test.title)} | ${escapeTable(test.severity)} | ${escapeTable(test.category)} |`).join("\n");
  return `---
id: overview
title: ${yamlQuote(config.title)}
sidebar_label: ${yamlQuote(config.sidebarLabel)}
sidebar_position: 1
className: generated-test-doc
hide_table_of_contents: true
description: ${yamlQuote(config.description)}
---

${generatedMarker}

# ${config.title}

${config.overview}

## Tests

| Test ID | Title | Severity | Category |
| --- | --- | --- | --- |
${rows}
`;
}

function renderRootIndex(tests) {
  const suiteRows = Object.entries(suiteConfig)
    .filter(([suite]) => tests.some((test) => test.suite === suite))
    .map(([suite, config]) => `| [${config.label}](./${suite}) | ${tests.filter((test) => test.suite === suite).length} | ${escapeTable(config.description)} |`)
    .join("\n");
  const testRows = tests
    .toSorted((a, b) => a.id.localeCompare(b.id))
    .map((test) => `| [${test.id}](./${test.id}) | ${escapeTable(test.title)} | ${escapeTable(suiteConfig[test.suite]?.label ?? test.suite)} | ${escapeTable(test.severity)} | ${escapeTable(test.category)} |`)
    .join("\n");
  return `---
id: overview
title: Tests Overview
sidebar_label: Tests Overview
sidebar_position: 1
className: generated-test-doc
hide_table_of_contents: true
description: ${yamlQuote("Searchable Maester test documentation generated from Pester tests, PowerShell help, severity configuration, and tags.")}
---

${generatedMarker}

# Tests Overview

This section is generated from the Maester test source. Each page includes the test ID, severity, tags, PowerShell command, overview, remediation details, and related references when available.

## Test Suites

| Suite | Tests | Description |
| --- | ---: | --- |
${suiteRows}

## All Tests

| Test ID | Title | Suite | Severity | Category |
| --- | --- | --- | --- | --- |
${testRows}
`;
}

function renderTagsIndex(tests) {
  const tagMap = new Map();
  for (const test of tests) {
    for (const tag of test.tags) {
      if (!tagMap.has(tag)) tagMap.set(tag, []);
      tagMap.get(tag).push(test);
    }
  }
  const rows = [...tagMap.entries()]
    .sort(([a], [b]) => a.localeCompare(b, undefined, { numeric: true }))
    .map(([tag, taggedTests]) => `| ${escapeTable(tag)} | ${taggedTests.length} | ${taggedTests.slice(0, 8).map((test) => `[${test.id}](../${test.id})`).join(", ")}${taggedTests.length > 8 ? ", ..." : ""} |`)
    .join("\n");
  return `---
id: overview
title: Tags Overview
sidebar_label: ${yamlQuote("🏷️ Tags")}
sidebar_position: 999
className: generated-test-doc
hide_table_of_contents: true
description: ${yamlQuote("Tags discovered from Maester Pester tests and linked to generated test documentation.")}
---

${generatedMarker}

# Tags Overview

Tags are discovered from Pester test metadata and can be used to find related Maester tests by suite, product area, benchmark, capability, or control ID.

| Tag | Tests | Examples |
| --- | ---: | --- |
${rows}
`;
}

function ensureDir(path) {
  mkdirSync(path, { recursive: true });
}

function writeGenerated(path, content, writes) {
  ensureDir(dirname(path));
  const normalized = `${content.trimEnd()}\n`;
  if (checkMode) {
    const existing = existsSync(path) ? readFileSync(path, "utf8") : "";
    if (existing !== normalized) writes.push(path);
    return;
  }
  writeFileSync(path, normalized);
}

function clearGeneratedDocs() {
  if (checkMode) return;
  for (const suite of Object.keys(suiteConfig)) {
    const suiteDir = join(docsTestsRoot, suite);
    if (existsSync(suiteDir)) rmSync(suiteDir, { recursive: true, force: true });
  }
  const tagsDir = join(docsTestsRoot, "tags");
  if (existsSync(tagsDir)) rmSync(tagsDir, { recursive: true, force: true });
}

function main() {
  const tests = buildInventory();
  const writes = [];
  clearGeneratedDocs();

  writeGenerated(join(docsTestsRoot, "readme.md"), renderRootIndex(tests), writes);
  writeGenerated(join(docsTestsRoot, "tags", "readme.md"), renderTagsIndex(tests), writes);

  for (const suite of Object.keys(suiteConfig)) {
    const suiteTests = tests.filter((test) => test.suite === suite);
    if (suiteTests.length === 0) continue;
    writeGenerated(join(docsTestsRoot, suite, "readme.md"), renderSuiteIndex(suite, suiteTests), writes);
    for (const test of suiteTests) {
      writeGenerated(pagePathFor(test), renderTestPage(test), writes);
    }
  }

  if (checkMode && writes.length > 0) {
    console.error(`Generated test docs are stale. Run npm run generate-test-docs. Stale files: ${writes.length}`);
    for (const file of writes.slice(0, 20)) console.error(`- ${relative(repoRoot, file)}`);
    process.exit(1);
  }

  console.log(`${checkMode ? "Checked" : "Generated"} ${tests.length} test documentation pages.`);
}

main();
